void solve(){
    int n,m; cin >> n >> m;
    int arr[100001];
    for(int i=0 ; i< n; i++){
        cin >> arr[i];
    }
    sort(arr,arr+n);

    int s=0,e=0,ans=1<<30,diff=0;

    while(1){
        diff = arr[e] - arr[s];
        if(diff >= m){
            ans = min(ans,diff);
            s++;
        }else{
            if(e== n) break;
            e++;
        }
    }
    cout << ans;


}


