void solve(){
    int n,s;
    cin >> n >> s;
    vector<int> cows(n);
    for(int i=0; i < n; i++){
        cin >> cows[i];
    }

    int ans=0;
    for(int i=0; i < cows.size(); i++){
        for(int j=i+1; j< cows.size(); j++){
            if(cows[i] + cows[j]  <=s) ans++;
        }
    }
    cout << ans;
    

}


