int dp[1000001][21];
const int mod= 1e9;

int go(int remain, int sqr){
    if(sqr == 0) return 1;
    if(remain == 0) return 1;
    int&ret = dp[remain][sqr];
    if(ret!=-1)return ret;
    ret = go(remain,sqr-1);
    int k = 1<<sqr;
    if(remain >= k ){
        ret = (ret + go(remain-k,sqr))%mod;
    }
    return ret;
}


