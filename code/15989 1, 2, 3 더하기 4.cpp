int go(int coin, int val){
    if(val > n) return 0;
    if(val == n) {
        return 1;
    }
    int &ret = dp[coin][val];
    if(ret!=-1) return ret;
    ret =go(coin,val+coin);
    if(coin+1 <= 3){
        ret += go(coin+1,val+coin+1);
    }
    if(coin+2 <=3){
        ret += go(coin+2,val+coin+2);
    }
    return ret;
}

