void go(string &s, int n, int carry){
    if(n==0) {
        if(carry==1){
            s[n]++;
        }
        return;
    }

    if(carry == 1){
        s[n] ++;
    }
    if(s[n] >= '5'){
        s[n] = '0';
        go(s,n-1,1);
    }else{
        s[n] = '0';
        go(s,n-1,0);
    }

}

